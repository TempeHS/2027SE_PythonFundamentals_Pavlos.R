# Prints the type of a list
#
# EXPLANATION:
# [] creates an empty list - a built-in Python type!
#
# KEY CONCEPTS:
# - [] is list literal syntax (shorthand)
# - Output: <class 'list'>
# - Lists are mutable (can be changed)
#
# LIST CREATION:
# - [] - empty list (literal syntax)
# - list() - also empty list (constructor syntax)
# Both create the same type of object!

print(type([]))

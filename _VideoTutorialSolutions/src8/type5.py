# Prints the type of a dictionary
#
# EXPLANATION:
# dict() is the constructor for the dict class!
#
# KEY CONCEPTS:
# - dict() calls the dict class constructor
# - Same as {}: both create an empty dictionary
# - Output: <class 'dict'>
#
# BUILT-IN CLASSES:
# Python has many built-in classes:
# - int, float (numbers)
# - str (strings)
# - list, tuple (sequences)
# - dict, set (collections)
#
# When we create our own classes like Student,
# we're extending Python with custom types!

print(type(dict()))

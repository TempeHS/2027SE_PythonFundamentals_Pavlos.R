# Prints the type of a dictionary
#
# EXPLANATION:
# {} creates an empty dictionary - another built-in type!
#
# KEY CONCEPTS:
# - {} is dict literal syntax (shorthand)
# - Output: <class 'dict'>
# - Dicts store key-value pairs
#
# DICT CREATION:
# - {} - empty dict (literal syntax)
# - dict() - also empty dict (constructor syntax)
# - {"name": "Harry"} - dict with data

print(type({}))

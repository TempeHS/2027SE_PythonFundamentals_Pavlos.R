# Prints the type of a list
#
# EXPLANATION:
# list() is the constructor for the list class!
#
# KEY CONCEPTS:
# - list() calls the list class constructor
# - Same as []: both create an empty list
# - Output: <class 'list'>
#
# CONSTRUCTOR CALLS:
# - list() creates a new list object
# - str() creates a new string object
# - int() creates a new integer object
# - Student() creates a new Student object!
#
# When we write Student(name, house), we're calling
# the Student class constructor, just like list()!

print(type(list()))

# Prints the type of a string
#
# EXPLANATION:
# Strings are also a type (class) in Python!
#
# KEY CONCEPTS:
# - "hello, world" is a str (string)
# - Output: <class 'str'>
# - Strings have many methods like .upper(), .lower(), .strip()
#
# STRING METHODS:
# "hello".upper()  -> "HELLO"
# "hello".capitalize()  -> "Hello"
# These work because str is a class with methods!

print(type("hello, world"))

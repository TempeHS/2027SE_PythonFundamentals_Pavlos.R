# Prints the type of an integer
#
# EXPLANATION:
# type() tells you what kind of data something is!
#
# KEY CONCEPTS:
# - type() returns the class/type of a value
# - 50 is an int (integer)
# - Output: <class 'int'>
#
# WHY THIS MATTERS FOR OOP:
# - int, str, list, dict are all TYPES (classes)
# - When we define 'class Student', we create a new TYPE
# - Every value in Python has a type
#
# EVERYTHING IS AN OBJECT:
# In Python, even simple values like 50 are objects.
# They have methods: (50).bit_length() returns 6

print(type(50))

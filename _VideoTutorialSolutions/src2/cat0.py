# Demonstrates multiple (identical) function calls
#
# EXPLANATION:
# This program prints "meow" three times - but in the most tedious way possible!
#
# KEY CONCEPTS:
# - This code WORKS, but it's repetitive and hard to maintain
# - What if you wanted to print "meow" 100 times? You'd need 100 lines!
# - What if you wanted to change "meow" to "woof"? You'd change 3 lines!
# - This is called "code duplication" and it's a BAD practice
#
# THE PROBLEM:
# Repetitive code is:
# - Tedious to write
# - Error-prone (easy to make typos)
# - Hard to maintain (changes need to happen in many places)
#
# THE SOLUTION:
# Loops! They let you repeat code without duplicating it.
# See cat1.py through cat7.py for better approaches.
#
# PROGRAMMING PRINCIPLE - DRY:
# "Don't Repeat Yourself" - if you're copying and pasting code,
# there's probably a better way using loops or functions.

print("meow")
print("meow")
print("meow")

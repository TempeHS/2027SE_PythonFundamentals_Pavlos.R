# Demonstrates a function with a positional argument
#
# EXPLANATION:
# This is your first Python program! It prints "hello, world" to the screen.
#
# KEY CONCEPTS:
# - print() is a built-in function that displays text on the screen
# - A "function" is a reusable piece of code that performs a specific task
# - The text inside the quotes is called a "string" - a sequence of characters
# - "hello, world" is passed as a "positional argument" to the print function
# - An argument is a value you give to a function to work with
#
# HOW IT WORKS:
# When Python runs this line, it calls the print() function and passes
# the string "hello, world" as input. The function then displays that text.

print("hello, world")

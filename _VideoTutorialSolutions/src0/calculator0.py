# Demonstrates addition
#
# EXPLANATION:
# This is the simplest calculator - it adds two numbers together.
#
# KEY CONCEPTS:
# - Variables store values: x = 1 stores the number 1 in variable x
# - The + operator adds numbers together
# - Unlike strings, when you use + with numbers, Python does math
# - print() can display numbers, not just text
#
# HOW IT WORKS:
# 1. x is assigned the value 1
# 2. y is assigned the value 2
# 3. z is assigned the result of x + y, which is 3
# 4. print(z) displays 3 on the screen
#
# DATA TYPES:
# - 1 and 2 are "integers" (int) - whole numbers without decimal points
# - Python automatically knows these are numbers, not text
#
# This is a simple example, but soon we'll get input from the user!

x = 1
y = 2

z = x + y

print(z)
